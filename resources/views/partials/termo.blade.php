   <h4>Termos e condições para uso dos armários</h4>
   <p>

       Por favor, leia atentamente os seguintes termos e condições antes de
       proceder com o uso dos armários disponibilizados pelo Instituto de
       Matemática e Estatística da Universidade de São Paulo (IME-USP):
   </p>
   <p>
       <strong>Elegibilidade de Uso:</strong> O uso dos armários é facultativo e restrito
       aos alunos com matrícula ativa em um dos programas de Pós-Graduação
       do IME-USP.
   </p>
   <p>
       <strong>Restrições de Armazenamento:</strong> Não é permitido armazenar alimentos,
       bebidas ou itens molhados nos armários, incluindo, mas não se
       limitando a guarda-chuvas, capas de chuva, entre outros. O usuário
       é responsável por garantir o cumprimento dessas restrições.
   </p>

   <p>
       <strong>Responsabilidade do Usuário:</strong> O IME-USP não se responsabiliza
       por extravio, danos ou perdas de objetos armazenados nos armários.
       Os pertences depositados são de total responsabilidade do usuário.
   </p>
   <p>
       <strong>Prazo de Desocupação: </strong> Ao confirmar o empréstimo do armário, o usuário
       declara estar ciente de que, ao defender sua tese/dissertação, terá um
       prazo máximo de 1 (um) mês para desocupá-lo. Após este período,
       os pertences serão retirados e guardados por mais 15 (quinze) dias,
       seguindo as seguintes diretrizes:
       Livros serão doados para a Biblioteca do Instituto.
       Objetos diversos serão doados, caso tenham utilidade.
   </p>

   <p>
       Ao proceder com a confirmação do empréstimo do armário, o usuário declara ter lido,
       compreendido e concordado com todos os termos e condições estabelecidos acima.
   </p>
